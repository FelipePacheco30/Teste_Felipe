import sys
from loguru import logger


def setup_logging():
    """
    Configura logging global da aplicação.
    Usado por ETL e API.
    """
    logger.remove()
    logger.add(
        sys.stdout,
        level="INFO",
        format=(
            "<green>{time:YYYY-MM-DD HH:mm:ss}</green> | "
            "<level>{level}</level> | "
            "{message}"
        ),
    )

    return logger


log = setup_logging()
